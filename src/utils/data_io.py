try:
    import h5py  # type: ignore
except ImportError:
    h5py = None  # type: ignore  # noqa: F405


class HDF5Exporter:
    """Write field data and simulation results to an HDF5 file.

    Parameters
    ----------
    filename : str
        Output HDF5 file path (e.g. ``"results.h5"``).

    Example
    -------
    >>> exporter = HDF5Exporter("demo.h5")
    >>> exporter.save_fields(E_theta, E_phi, theta_deg, phi_deg)
    """

    def __init__(self, filename: str) -> None:
        if h5py is None:
            warnings.warn(
                "h5py is not installed. HDF5 functionality will be disabled."
            )
        self.filename = filename

    def save_fields(
        self,
        E_theta: np.ndarray,
        E_phi: np.ndarray,
        theta_deg: np.ndarray,
        phi_deg: np.ndarray,
        frequency_Hz: float = 1e9,
        S_params: Optional[np.ndarray] = None,
        currents: Optional[np.ndarray] = None,
    ) -> None:
        """Save field data and/or simulation results to HDF5.

        Creates groups::

            /fields/E_theta   (array)
            /fields/E_phi     (array)
            /metadata/frequency  (scalar or array)
            /results/S_params    (optional)
            /results/currents    (optional)

        Parameters
        ----------
        E_theta : np.ndarray
            Theta-component of the far-field.
        E_phi : np.ndarray
            Phi-component of the far-field.
        theta_deg : np.ndarray
            Polar angles in degrees.
        phi_deg : np.ndarray
            Azimuthal angles in degrees.
        frequency_Hz : float, optional
            Simulation frequency in Hz. Default 1e9.
        S_params : np.ndarray or None
            Optional S-parameter array.
        currents : np.ndarray or None
            Optional current distribution array.
        """
        if h5py is None:
            raise RuntimeError(
                "h5py is not installed. Install it via ``pip install h5py``."
            )

        with h5py.File(self.filename, "w") as hf:
            # Field data
            fields = hf.create_group("fields")
            fields.create_dataset("E_theta", data=E_theta)
            fields.create_dataset("E_phi", data=E_phi)

            # Metadata
            meta = hf.create_group("metadata")
            meta.create_dataset("frequency_Hz", data=frequency_Hz)
            meta.create_dataset("theta_deg", data=theta_deg)
            meta.create_dataset("phi_deg", data=phi_deg)

            # Optional results
            if S_params is not None:
                res = hf.create_group("results")
                res.create_dataset("S_params", data=S_params)

            if currents is not None:
                res = hf.require_group("results")
                res.create_dataset("currents", data=currents)


class HDF5Importer:
    """Read field data and simulation results from an HDF5 file.

    Parameters
    ----------
    filename : str
        Path to the HDF5 file (e.g. ``"results.h5"``).

    Example
    -------
    >>> importer = HDF5Importer("demo.h5")
    >>> fields = importer.load_fields()
    >>> metadata = importer.load_metadata()
    """

    def __init__(self, filename: str) -> None:
        if h5py is None:
            warnings.warn(
                "h5py is not installed. HDF5 functionality will be disabled."
            )
        self.filename = filename

    def load_fields(self) -> dict:
        """Load field data from the HDF5 file.

        Returns
        -------
        dict
            Dictionary with keys ``E_theta``, ``E_phi``, ``theta_deg``,
            ``phi_deg`` as NumPy arrays.
        """
        if h5py is None:
            raise RuntimeError(
                "h5py is not installed. Install it via ``pip install h5py``."
            )

        with h5py.File(self.filename, "r") as hf:
            fields = hf["fields"]
            return {
                "E_theta": np.asarray(fields["E_theta"]),
                "E_phi": np.asarray(fields["E_phi"]),
                "theta_deg": np.asarray(fields["theta_deg"]),
                "phi_deg": np.asarray(fields["phi_deg"]),
            }

    def load_metadata(self) -> dict:
        """Load the metadata group.

        Returns
        -------
        dict
            Dictionary with keys from ``/metadata`` (e.g. ``frequency_Hz``).
        """
        if h5py is None:
            raise RuntimeError(
                "h5py is not installed. Install it via ``pip install h5py``."
            )

        with h5py.File(self.filename, "r") as hf:
            meta = hf["metadata"]
            return {k: np.asarray(meta[k]) for k in meta.keys()}


